# 2-7 Stripping Names: Use a variable to represent a person's name, and then print that person's name with whitespace characters at the beginning and end of the name. Print the name using each of the three stripping methods: lstrip(), rstrip(), and strip().
name = "\tFurina \n"
print(name)
print(name.lstrip())
print(name.rstrip())
print(name.strip())